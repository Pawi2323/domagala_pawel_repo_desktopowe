    private void addKeyListenerToFirst(){
        jTFpierwszy.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                String temp = jTFpierwszy.getText();
                char pd_ch = e.getKeyChar();
                if((pd_ch >= 'A' && pd_ch <= 'F' || pd_ch=='-'||pd_ch==KeyEvent.VK_BACK_SPACE)){
                    jTFpierwszy.setEditable(true);
                }else{
                    jTFpierwszy.setEditable(false);
                }
                
            }

            @Override
            public void keyPressed(KeyEvent e) {
                
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        });
    }
    
    private void addKeyListenerToNumbers(){
        jTFcyfry.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                String temp = jTFcyfry.getText();
                char pd_ch = e.getKeyChar();
                if((pd_ch >= '0' && pd_ch <= '9' || pd_ch==KeyEvent.VK_BACK_SPACE)){
                    jTFcyfry.setEditable(true);
                }else{
                    jTFcyfry.setEditable(false);
                }
                
            }

            @Override
            public void keyPressed(KeyEvent e) {
                
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        });
    }
    
    private void Zapis(){
        File f = new File("pawel_domagala.txt");
        FileWriter fw;
        try {
            fw = new FileWriter(f,true);
            fw.append(jTFpierwszy.getText()+"/"+jTFcyfry.getText()+"\n");
            fw.close();
        } catch (IOException ex) {
            
        }
    }

